// Object.values devuelve un array con los valores de cada propiedad de un objeto.
const countries = { MX: 'México', CO: 'Colombia', CL: 'Chile', PE: 'Perú' };
console.log(Object.values(countries));

//Object.keys devuelve un array con todas las propiedades de un objeto (sin sus valores)
console.log(Object.keys(countries));